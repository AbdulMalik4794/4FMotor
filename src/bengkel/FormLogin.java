    private void btnLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLoginActionPerformed
        // TODO add your handling code here:
        if (cmBagian.getSelectedItem().equals("Pegawai")){
            db.Connect();
            try{
                st = db.getConnection().createStatement();
                String sql = "SELECT * FROM pegawai WHERE username = '"+txtUsername.getText()+"' and password = '"+txtPassword.getText()+"'";
                    rs = st.executeQuery(sql);
                    if(rs.next()){
                        FormMenu mn = new FormMenu(rs.getString("id_pegawai"));
                        mn.setVisible(true);
                        this.dispose();
                    }
                    else{
                        JOptionPane.showMessageDialog(rootPane, "Gagal Login", "Pesan", JOptionPane.ERROR_MESSAGE);
                    }
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(null, "Koneksi Database Terputus !");
            }
        }else if (cmBagian.getSelectedItem().equals("Pemilik")){
            db.Connect();
            try{
                st = db.getConnection().createStatement();
                String sql = "SELECT * FROM pemilik WHERE username = '"+txtUsername.getText()+"' and password = '"+txtPassword.getText()+"'";
                    rs = st.executeQuery(sql);
                    if(rs.next()){
                        FormMenu mn = new FormMenu(rs.getString("id_pemilik"));
                        mn.setVisible(true);
                        this.dispose();
                    }
                    else{
                        JOptionPane.showMessageDialog(rootPane, "Gagal Login", "Pesan", JOptionPane.ERROR_MESSAGE);
                    }
            }
            catch(SQLException e){
                JOptionPane.showMessageDialog(null, "Koneksi Database Terputus !");
            }
        }else{
            JOptionPane.showMessageDialog(rootPane, "Gagal Login", "Pesan", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnLoginActionPerformed

