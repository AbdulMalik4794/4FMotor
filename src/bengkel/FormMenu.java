    private void setuser(){
        db.Connect();
        try{
            String sql = "SELECT * FROM pegawai WHERE id_pegawai = '"+id_pegawai+"'";
            st = db.getConnection().createStatement();
            rs = st.executeQuery(sql);
            if(rs.next()){
                txtInfoUsername.setText(rs.getString("username"));
                txtInfoPassword.setText(rs.getString("password"));
            }
        } catch (SQLException ex) {
            Logger.getLogger(FormMenu.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
