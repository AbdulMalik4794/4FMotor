    private void CetakLaporan(String sourcefilename, HashMap hash){
        cn = db.getConnection();
        try {
            InputStream report;
            report = getClass().getResourceAsStream(sourcefilename);
            JasperPrint jprint = JasperFillManager.fillReport(report,hash, cn);
            JasperViewer viewer = new JasperViewer(jprint, false);
            viewer.setFitPageZoomRatio();
            viewer.setVisible(true);
        } catch (Exception e) {
            System.out.print(e.getMessage());
        }
    }
    private void GantiPass(){
        if(id_pegawai.equals("PM01")){
            db.Connect();
            try{
                String sql = "update pemilik set username = '"+txtInfoUsername.getText()
                        +"',password = '"+txtInfoPassword.getText()
                        +"' WHERE id_pemilik = '"+id_pegawai+"'";
                st = db.getConnection().createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Ganti Password Berhasil");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Gagal Mengubah Password !");
            }
        }else{
            db.Connect();
            try{
                String sql = "update pegawai set username = '"+txtInfoUsername.getText()
                        +"',password = '"+txtInfoPassword.getText()
                        +"' WHERE id_pegawai = '"+id_pegawai+"'";
                st = db.getConnection().createStatement();
                st.executeUpdate(sql);
                JOptionPane.showMessageDialog(null, "Ganti Password Berhasil");
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Gagal Mengubah Password !");
            }
        }
    }
